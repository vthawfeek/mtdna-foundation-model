def _base_encoder_and_vocab(base_model_path: str = "models/phase1_v1"):
    """Load the Phase 1 base encoder and vocabulary."""
    from mtdna_fm.model.model import MtDNAForMaskedModeling
    from mtdna_fm.tokenizer.vocabulary import KmerVocabulary

    full_model = MtDNAForMaskedModeling.from_pretrained(base_model_path)
    base_encoder = full_model.mtdna
    vocab = KmerVocabulary.from_pretrained(base_model_path)
    return base_encoder, vocab


def load_haplogroup_model(adapter_path: str, base_model_path: str = "models/phase1_v1"):
    """Load the PEFT haplogroup classification model."""
    from mtdna_fm.model.model import MtDNAForMaskedModeling, MtDNAForHaplogroupClassification
    from mtdna_fm.tokenizer.vocabulary import KmerVocabulary
    from peft import PeftModel

    ckpt = Path(adapter_path)
    if not ckpt.exists():
        raise FileNotFoundError(
            f"Adapter not found: {adapter_path}\n"
            "Run fine-tuning first:\n"
            "  uv run mtdna-finetune --task haplogroup "
            "--config configs/finetuning_haplogroup_paper.yaml"
        )

    logger.info(f"Loading haplogroup model from {adapter_path}")
    full_model = MtDNAForMaskedModeling.from_pretrained(base_model_path)
    base_encoder = full_model.mtdna
    vocab = KmerVocabulary.from_pretrained(base_model_path)

    task_model = MtDNAForHaplogroupClassification(base_encoder, num_labels=26)
    model = PeftModel.from_pretrained(task_model, adapter_path)
    model.eval()
    logger.info(f"  Haplogroup model loaded ({sum(p.numel() for p in model.parameters()):,} params)")
    return model, vocab


def load_pathogenicity_model(adapter_path: str, base_model_path: str = "models/phase1_v1"):
    """Load the PEFT pathogenicity prediction model."""
    from mtdna_fm.model.model import MtDNAForMaskedModeling, MtDNAForVariantPathogenicity
    from mtdna_fm.tokenizer.vocabulary import KmerVocabulary
    from peft import PeftModel

    ckpt = Path(adapter_path)
    if not ckpt.exists():
        raise FileNotFoundError(
            f"Adapter not found: {adapter_path}\n"
            "Run fine-tuning first:\n"
            "  uv run mtdna-finetune --task pathogenicity "
            "--config configs/finetuning_pathogenicity_paper.yaml"
        )

    logger.info(f"Loading pathogenicity model from {adapter_path}")
    full_model = MtDNAForMaskedModeling.from_pretrained(base_model_path)
    base_encoder = full_model.mtdna
    vocab = KmerVocabulary.from_pretrained(base_model_path)

    task_model = MtDNAForVariantPathogenicity(base_encoder, pos_weight=2.5)
    model = PeftModel.from_pretrained(task_model, adapter_path)
    model.eval()
    return model, vocab


# ---------------------------------------------------------------------------
# Haplogroup inference
# ---------------------------------------------------------------------------

def run_haplogroup_inference(
    model,
    vocab,
    test_parquet: str = "data/processed/test.parquet",
) -> pd.DataFrame:
    """Run haplogroup classification on the test set.

    Returns DataFrame with columns: accession, true_haplogroup, true_label, predicted_label,
    predicted_haplogroup, correct, probabilities.
    """
    from mtdna_fm.tokenizer.tokenize import tokenize_sequence

    df = pd.read_parquet(test_parquet)

    # Map to major haplogroups
    df["major_haplogroup"] = df["haplogroup"].apply(map_to_major)
    eligible = df[df["major_haplogroup"].notna()].copy()
    logger.info(
        f"Haplogroup inference: {len(eligible)}/{len(df)} sequences "
        f"map to a major haplogroup"
    )

    if eligible.empty:
        logger.error("No eligible sequences — run fix_haplogroup_labels.py first")
        return pd.DataFrame()

    records = []
    with torch.no_grad():
        for i, (_, row) in enumerate(eligible.iterrows()):
            seq = row["sequence"]
            major = row["major_haplogroup"]
            true_idx = LABEL2IDX[major]

            # Tokenize into windows
            tokens = tokenize_sequence(
                seq, vocab, k=6, stride=1, max_seq_len=len(seq), circular=True
            )
            n_tokens = len(tokens["input_ids"])

            # Sliding windows (stride 256, window 512)
            window_logits = []
            for start in range(0, n_tokens, 256):
                end = min(start + 512, n_tokens)
                if end - start < 10:
                    continue
                window_ids = tokens["input_ids"][start:end]
                pos_ids = tokens["position_ids"][start:end]
                het_vals = tokens["het_values"][start:end]
                mask = tokens["attention_mask"][start:end]

                input_ids = torch.tensor([window_ids], dtype=torch.long)
                position_ids = torch.tensor([pos_ids], dtype=torch.long)
                het_values = torch.tensor([het_vals], dtype=torch.float)
                attention_mask = torch.tensor([mask], dtype=torch.long)

                out = model(
                    input_ids=input_ids,
                    position_ids=position_ids,
                    het_values=het_values,
                    attention_mask=attention_mask,
                )
                window_logits.append(out.logits.squeeze(0).cpu().numpy())

            if not window_logits:
                continue

            # Mean-pool window logits → majority vote
            mean_logits = np.mean(window_logits, axis=0)
            probs = torch.softmax(torch.tensor(mean_logits), dim=0).numpy()
            pred_idx = int(np.argmax(mean_logits))

            records.append({
                "accession": row.get("accession", f"seq_{i}"),
                "true_haplogroup": major,
                "true_label": true_idx,
                "predicted_label": pred_idx,
                "predicted_haplogroup": IDX2LABEL[pred_idx],
                "correct": int(pred_idx == true_idx),
                "max_prob": float(probs[pred_idx]),
            })

            if (i + 1) % 100 == 0:
                logger.info(f"  {i + 1}/{len(eligible)} sequences processed")

    result_df = pd.DataFrame(records)
    if not result_df.empty:
        acc = result_df["correct"].mean()
        logger.info(f"Haplogroup accuracy (REAL): {acc:.4f} on {len(result_df)} sequences")
    return result_df


