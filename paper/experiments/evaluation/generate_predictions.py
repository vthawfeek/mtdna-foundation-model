"""
Generate per-sample predictions from the fine-tuned PEFT models.

Loads the LoRA-adapted haplogroup and pathogenicity models, runs inference on the
test set, and saves per-sample prediction parquets needed for bootstrap CI computation.

NOTE: The existing reports/eval_summary.json contains SYNTHETIC numbers generated by
the mtdna-evaluate CLI (which falls back to synthetic data when no real eval parquets
exist). This script generates REAL numbers from the actual models.

Prerequisites:
  1. run fix_haplogroup_labels.py     → adds major_haplogroup to parquets
  2. run prepare_variant_data.py      → creates variant parquets
  3. run fine-tuning (see below)      → creates model checkpoints

After fine-tuning with paper configs:
  uv run mtdna-finetune --task haplogroup --config configs/finetuning_haplogroup_paper.yaml
  uv run mtdna-finetune --task pathogenicity --config configs/finetuning_pathogenicity_paper.yaml

Usage:
    uv run python paper/experiments/evaluation/generate_predictions.py

Outputs:
    reports/eval_haplogroup_predictions.parquet
    reports/eval_variant_predictions.parquet
    reports/real_eval_summary.json        ← replaces synthetic eval_summary.json numbers
"""

from __future__ import annotations

import json
import logging
from pathlib import Path

import numpy as np
import pandas as pd
import torch

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
logger = logging.getLogger(__name__)

REPORTS_DIR = Path("reports")
REPORTS_DIR.mkdir(parents=True, exist_ok=True)

# Haplogroup label ordering (must match HaplogroupWindowDataset.HAPLOGROUPS)
HAPLOGROUPS = [
    "A", "B", "C", "D", "E", "F", "G", "H", "HV", "I",
    "J", "K", "L0", "L1", "L2", "L3", "L4", "L5", "M",
    "N", "R", "T", "U", "V", "W", "X",
]
LABEL2IDX = {h: i for i, h in enumerate(HAPLOGROUPS)}
IDX2LABEL = {i: h for i, h in enumerate(HAPLOGROUPS)}


def map_to_major(hap: str | None) -> str | None:
    """Map detailed haplogroup to major haplogroup (mirrors fix_haplogroup_labels.py)."""
    if not hap:
        return None
    hap = hap.strip()
    if hap.startswith("L") and len(hap) > 1 and hap[1].isdigit():
        clade = "L" + hap[1]
        return clade if clade in LABEL2IDX else None
    if hap.startswith("HV"):
        return "HV"
    first = hap[0].upper()
    return first if first in LABEL2IDX else None


