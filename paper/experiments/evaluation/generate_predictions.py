def run_pathogenicity_inference(
    model,
    vocab,
    test_parquet: str = "data/processed/variants_pathogenicity_val.parquet",
) -> pd.DataFrame:
    """Run pathogenicity prediction on the variant test set."""
    from mtdna_fm.tokenizer.tokenize import tokenize_sequence

    if not Path(test_parquet).exists():
        logger.warning(f"Variant test parquet not found: {test_parquet}")
        logger.warning("Run prepare_variant_data.py first")
        return pd.DataFrame()

    df = pd.read_parquet(test_parquet)
    logger.info(f"Pathogenicity inference on {len(df)} variants (label=1: {df['label'].sum()})")

    records = []
    with torch.no_grad():
        for i, (_, row) in enumerate(df.iterrows()):
            seq = str(row["sequence"])
            pos_in_window = int(row["position"])
            true_label = int(row["label"])

            tokens = tokenize_sequence(
                seq, vocab, k=6, stride=1, max_seq_len=min(len(seq), 512), circular=False
            )
            n = len(tokens["input_ids"])
            var_tok_idx = min(pos_in_window, n - 1)

            input_ids = torch.tensor([tokens["input_ids"]], dtype=torch.long)
            position_ids = torch.tensor([tokens["position_ids"]], dtype=torch.long)
            het_values = torch.tensor([tokens["het_values"]], dtype=torch.float)
            attention_mask = torch.tensor([tokens["attention_mask"]], dtype=torch.long)
            variant_token_idx = torch.tensor([var_tok_idx], dtype=torch.long)

            out = model(
                input_ids=input_ids,
                position_ids=position_ids,
                variant_token_idx=variant_token_idx,
                het_values=het_values,
                attention_mask=attention_mask,
            )
            prob = float(out.probs.squeeze().item())

            records.append({
                "label": true_label,
                "score": prob,
                "variant_type": row.get("variant_type", "unknown"),
                "rCRS_pos": row.get("rCRS_pos", -1),
            })

    result_df = pd.DataFrame(records)
    if not result_df.empty:
        from sklearn.metrics import roc_auc_score, average_precision_score
        auroc = roc_auc_score(result_df["label"], result_df["score"])
        aupr = average_precision_score(result_df["label"], result_df["score"])
        logger.info(f"Pathogenicity AUROC (REAL): {auroc:.4f}, AUPR: {aupr:.4f}")
    return result_df


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    # Try paper models first, fall back to v1 adapters
    hap_adapter = "models/finetune_haplogroup_paper"
    if not Path(hap_adapter).exists():
        hap_adapter = "models/finetune_haplogroup_v1"
        logger.warning(
            f"Paper haplogroup model not found; using {hap_adapter}.\n"
            "For best results, run fine-tuning with proper label mapping first:\n"
            "  1. uv run python paper/experiments/evaluation/fix_haplogroup_labels.py\n"
            "  2. uv run mtdna-finetune --task haplogroup "
            "--config configs/finetuning_haplogroup_paper.yaml"
        )

    path_adapter = "models/finetune_pathogenicity_paper"
    if not Path(path_adapter).exists():
        path_adapter = "models/finetune_pathogenicity_v1"
        logger.warning(f"Paper pathogenicity model not found; using {path_adapter}")

    results = {}

    # --- Haplogroup inference ---
    try:
        hap_model, hap_vocab = load_haplogroup_model(hap_adapter)
        hap_df = run_haplogroup_inference(hap_model, hap_vocab)
        if not hap_df.empty:
            out = REPORTS_DIR / "eval_haplogroup_predictions.parquet"
            hap_df.to_parquet(out, index=False)
            logger.info(f"Saved haplogroup predictions → {out}")
            results["haplogroup_accuracy_real"] = float(hap_df["correct"].mean())
            results["haplogroup_n_sequences"] = len(hap_df)
    except FileNotFoundError as e:
        logger.error(str(e))

    # --- Pathogenicity inference ---
    try:
        path_model, path_vocab = load_pathogenicity_model(path_adapter)
        path_df = run_pathogenicity_inference(path_model, path_vocab)
        if not path_df.empty:
            out = REPORTS_DIR / "eval_variant_predictions.parquet"
            path_df.to_parquet(out, index=False)
            logger.info(f"Saved pathogenicity predictions → {out}")
            from sklearn.metrics import roc_auc_score, average_precision_score
            results["pathogenicity_auroc_real"] = float(
                roc_auc_score(path_df["label"], path_df["score"])
            )
            results["pathogenicity_n_variants"] = len(path_df)
    except FileNotFoundError as e:
        logger.error(str(e))

    # Save real eval summary
    real_summary_path = REPORTS_DIR / "real_eval_summary.json"
    real_summary_path.write_text(json.dumps(results, indent=2))
    logger.info(f"\nReal evaluation summary saved → {real_summary_path}")

    print("\n=== REAL Model Performance ===")
    if "haplogroup_accuracy_real" in results:
        print(f"Haplogroup accuracy:   {results['haplogroup_accuracy_real']:.4f}  "
              f"(n={results['haplogroup_n_sequences']})")
    else:
        print("Haplogroup: not yet evaluated (run fine-tuning first)")

    if "pathogenicity_auroc_real" in results:
        print(f"Pathogenicity AUROC:   {results['pathogenicity_auroc_real']:.4f}  "
              f"(n={results['pathogenicity_n_variants']})")
    else:
        print("Pathogenicity: not yet evaluated (run prepare_variant_data.py + fine-tuning first)")

    print(f"\nNOTE: Previous eval_summary.json used SYNTHETIC data.")
    print(f"These are the actual model predictions.")


if __name__ == "__main__":
    main()
